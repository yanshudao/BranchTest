package io.renren.modules.zd.form;

public class ZdCatResourceFrom {
    private String id;
    private String majorId;
    private String courseId;
    private String resourceId;
    private String zmcrId;
    private Integer catNum;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMajorId() {
        return majorId;
    }

    public void setMajorId(String majorId) {
        this.majorId = majorId;
    }

    public String getCourseId() {
        return courseId;
    }

    public void setCourseId(String courseId) {
        this.courseId = courseId;
    }

    public String getResourceId() {
        return resourceId;
    }

    public void setResourceId(String resourceId) {
        this.resourceId = resourceId;
    }

    public Integer getCatNum() {
        return catNum;
    }

    public void setCatNum(Integer catNum) {
        this.catNum = catNum;
    }

    public String getZmcrId() {
        return zmcrId;
    }

    public void setZmcrId(String zmcrId) {
        this.zmcrId = zmcrId;
    }
}
